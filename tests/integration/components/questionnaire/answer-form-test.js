import { module, test } from 'qunit';
import { setupRenderingTest } from 'ember-qunit';
import { render } from '@ember/test-helpers';
import hbs from 'htmlbars-inline-precompile';

module('Integration | Component | questionnaire/answer form', function(hooks) {
  setupRenderingTest(hooks);

  test('it renders', async function(assert) {
    // Set any properties with this.set('myProperty', 'value');
    // Handle any actions with this.on('myAction', function(val) { ... });

    await render(hbs`{{questionnaire/answer-form}}`);

    assert.notEqual(this.$().text().trim().indexOf('Job Name:'), -1);
    assert.notEqual(this.$().text().trim().indexOf('Fund Code:'), -1);
  });
});
